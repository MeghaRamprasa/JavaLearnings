package automation.basics.com;

public class RemainingLoop {

	public static void main(String[] args) {
		int a=1;
		do{
			System.out.println(a);
			a++;
		}while(a>3);
		System.out.println("value after loop broke " +a);
	
	
	
	int b=1;
	while(b>2)
	{
		System.out.println("value of a is " +b);
	}
	
	int c=5;
	while(b>2)
	{
		System.out.println("value of a is " +c);
		c--;
	}


}
}
